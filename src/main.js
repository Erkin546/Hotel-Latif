let currentLang = 'ru';

function updateLanguage(lang) {
    currentLang = lang;
    document.getElementById('langToggle').textContent = lang === 'ru' ? 'En' : 'Ru';

    document.querySelectorAll('[data-key]').forEach(el => {
        const key = el.dataset.key;
        if (translations[lang] && translations[lang][key] !== undefined) {
            el.innerHTML = translations[lang][key];
        }
    });

    // Обновляем заголовки номеров
    document.querySelectorAll('.room-info h3').forEach(h3 => {
        const enSpan = h3.querySelector('span[data-key*="title"]:not([data-key*="ru"])');
        const ruSpan = h3.querySelector('span[data-key*="title_ru"]');
        if (enSpan && ruSpan) {
            if (lang === 'ru') {
                enSpan.style.display = 'none';
                ruSpan.style.display = 'inline';
            } else {
                enSpan.style.display = 'inline';
                ruSpan.style.display = 'none';
            }
        }
    });
}

document.getElementById('langToggle').addEventListener('click', function() {
    const nextLang = currentLang === 'ru' ? 'en' : 'ru';
    updateLanguage(nextLang);
});

